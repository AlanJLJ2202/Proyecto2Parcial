    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if(tProductos.getSelectedRow()>= 0){
            int idProductos = (int) tProductos.getValueAt(tProductos.getSelectedRow(), 0);
            frmNProducto obj = new frmNProducto(idProductos);
            obj.setTitle("Modificar producto");
            obj.setModal(true);
            obj.setVisible(true);
            tProductos.setModel(new Productos().GetAllModel());
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar un registro");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
         
      if (tProductos.getSelectedRow() >= 0) {
            int idProducto = (int) tProductos.getValueAt(tProductos.getSelectedRow(), 0);
            Productos obj = new Productos();
            obj.setIdProducto(idProducto);
            obj.Delete();
            tProductos.setModel(new Productos().GetAllModel());
        }else{
            JOptionPane.showMessageDialog(null, "Debes de seleccionar un registro");
        } 
      


     
    }//GEN-LAST:event_btnEliminarActionPerformed

